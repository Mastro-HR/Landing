// Enhanced translation hook with type checking and fallbacks
export const useAssessmentTranslation = (lang = 'en') => {
  const getTranslation = (key, defaultValue = '') => {
    try {
      const keys = key.split('.');
      let result = assessmentTranslations[lang] || assessmentTranslations['en']; // Fallback to English

      for (const k of keys) {
        if (result && Object.prototype.hasOwnProperty.call(result, k)) {
          result = result[k];
        } else {
          console.warn(`Translation missing for key: ${key} in language: ${lang}`);
          // Try English version before falling back to default
          const englishVersion = getEnglishTranslation(key);
          return englishVersion || defaultValue || key;
        }
      }

      return result;
    } catch (error) {
      console.error(`Translation error for key: ${key}`, error);
      return defaultValue || key;
    }
  };

  const getEnglishTranslation = (key) => {
    const keys = key.split('.');
    let result = assessmentTranslations['en'];

    for (const k of keys) {
      if (result && Object.prototype.hasOwnProperty.call(result, k)) {
        result = result[k];
      } else {
        return null;
      }
    }

    return result;
  };

  const getMetrics = (stage) => {
    const stageData = getTranslation(`pipeline.stages.${stage}`);
    if (!stageData || !stageData.metrics) return [];

    return Object.entries(stageData.metrics).map(([key, value]) => ({
      type: value.type,
      title: value.title,
      description: value.description,
      icon: getIconForMetricType(value.type, key)
    }));
  };

  const getIconForMetricType = (type, key) => {
    const iconMap = {
      expertise: {
        technical_depth: 'Brain',
        leadership: 'Shield',
        performance: 'LineChart'
      },
      performance: {
        innovation: 'LineChart',
        domain: 'Target',
        development: 'Target'
      }
    };

    return iconMap[type]?.[key] || 'Brain';
  };

  return {
    t: getTranslation,
    getMetrics
  };
}